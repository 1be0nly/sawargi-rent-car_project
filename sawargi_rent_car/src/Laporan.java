    private void Print_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Print_BtnActionPerformed
    if(Tglakhir.getText().equals("") || Tglawal.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Isi Tanggal Laporan!");
        }
    else {try {
            File namafile = new File("src/laporan.jasper");
            HashMap hash = new HashMap(2);

            hash.put("Awal",awal);
            hash.put("Akhir",akhir);
            JasperPrint jp = JasperFillManager.fillReport(namafile.getPath(), hash, Koneksi.getConnection());
            JasperViewer.viewReport(jp, false);
        } catch (JRException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        } }       // TODO add your handling code here:
    }//GEN-LAST:event_Print_BtnActionPerformed

