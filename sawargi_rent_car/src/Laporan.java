    DefaultTableModel dtm,lapor;
        private void penyewa(){
            
            String[] kolom = {"NO_ID", "Nama", "NIK", "Telp", "Alamat"};
            
            dtm = new DefaultTableModel(null, kolom);
            try {
                Statement stmt = koneksi.createStatement();
                String sql = "SELECT * FROM tb_penyewa";
                ResultSet rs = stmt.executeQuery(sql);
                
            while(rs.next()){
                String ID = rs.getString("No_ID");
                String Nama = rs.getString("Nama");
                String Nik = rs.getString("Nik");
                String Telp = rs.getString("Telp");
                String Alm = rs.getString("Alamat");
            
                dtm.addRow(new String[]{ID, Nama, Nik, Telp, Alm});      
            }
        }
        catch (SQLException ex) {
            ex.printStackTrace();
        }
        tbl_penyewa.setModel(dtm);}
        
        private void tampilkan() {
        int row = tbl_penyewa.getRowCount();
        for(int a=0;a<row;a++) {
            dtm.removeRow(0);
        }
        String cari = txtcari.getText();
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/sawargi_rent_car", "root", "");
            ResultSet rs = cn.createStatement().executeQuery("SELECT * From tb_penyewa WHERE nama LIKE '%"+cari+"%'");
            
            while (rs.next()) {
                String data[] = {rs.getString(1),rs.getString(2),rs.getString(3), rs.getString(4), rs.getString(5)};
            dtm.addRow(data);
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }
        private void laporan(){
            
            String[] kolom = {"id_trans", "Nama", "Kendaraan", "Nopol", "Supir", "Tgl Sewa", "Tgl Kembali", "Jumlah Hari", "Total"};
            
            lapor = new DefaultTableModel(null, kolom);
            try {
                Statement stmt = koneksi.createStatement();
                String sql = "SELECT * FROM tb_kembali";
                ResultSet rs = stmt.executeQuery(sql);
                
            while(rs.next()){
                String ID = rs.getString("id_trans");
                String Nama = rs.getString("Nama");
                String Kenda = rs.getString("Kendaraan");
                String Nopol = rs.getString("Nopol");
                String Supir = rs.getString("Supir");
                String Sewa = rs.getString("Tgl_sewa");
                String Kem = rs.getString("Tgl_kembali");
                String Jum = rs.getString("Jum_hari");
                String Tot = rs.getString("Total");
            
                lapor.addRow(new String[]{ID, Nama, Kenda, Nopol, Supir, Sewa, Kem, Jum, Tot});      
            }
        }
        catch (SQLException ex) {
            ex.printStackTrace();
        }
        tbl_laporan.setModel(lapor);}
