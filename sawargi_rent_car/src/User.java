     private void kode() {
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/sawargi_rent_car", "root", "");
            Statement s = cn.createStatement();

            String sql = "SELECT * FROM `tb_user` ORDER by `id` desc";
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                String noo = r.getString("id").substring(2);
                String ANN = "" + (Integer.parseInt(noo) + 1);
                String Noll = "";

                if (ANN.length() == 1) {
                    Noll = "00";
                } else if (ANN.length() == 2) {
                    Noll = "0";
                } else if (ANN.length() == 3) {
                    Noll = "";
                } 

                id.setText("KU" + Noll + ANN);
            } else {
                id.setText("KU001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }}
