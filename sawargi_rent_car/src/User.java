 private void tampilkan() {
        int row = tabeluser.getRowCount();
        for(int a=0;a<row;a++) {
            model.removeRow(0);
        }
        
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/sawargi_rent_car", "root", "");
            ResultSet rs = cn.createStatement().executeQuery("SELECT * From tb_user");
            
            while (rs.next()) {
                String data[] = {rs.getString(1),rs.getString(2),rs.getString(3)};
            model.addRow(data);
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

}
