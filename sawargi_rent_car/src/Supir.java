    private void tampilkan() {
        int row = tabelSupir.getRowCount();
        for(int a=0;a<row;a++) {
            model.removeRow(0);
        }
        String cari = txtcari.getText();
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/sawargi_rent_car", "root", "");
            ResultSet rs = cn.createStatement().executeQuery("SELECT * From tb_supir WHERE nama LIKE '%"+cari+"%'");
            
            while (rs.next()) {
                String data[] = {rs.getString(1),rs.getString(2),rs.getString(3), rs.getString(4)};
            model.addRow(data);
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }
