     private void kode() {
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/sawargi_rent_car", "root", "");
            Statement s = cn.createStatement();

            String sql = "SELECT * FROM `tb_supir` ORDER by `id_supir` desc";
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                String no = r.getString("id_supir").substring(2);
                String AN = "" + (Integer.parseInt(no) + 1);
                String Nol = "";

                if (AN.length() == 1) {
                    Nol = "00";
                } else if (AN.length() == 2) {
                    Nol = "0";
                } else if (AN.length() == 3) {
                    Nol = "";
                } 

                id.setText("KS" + Nol + AN);
            } else {
                id.setText("KS001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }}
