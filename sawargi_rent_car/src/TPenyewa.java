    private void kode() {
        try {
            Statement s = koneksi.createStatement();

            String sql = "SELECT * FROM `tb_penyewa` ORDER by `No_ID` desc";
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                String noo = r.getString("No_ID").substring(2);
                String ANN = "" + (Integer.parseInt(noo) + 1);
                String Noll = "";

                if (ANN.length() == 1) {
                    Noll = "00";
                } else if (ANN.length() == 2) {
                    Noll = "0";
                } else if (ANN.length() == 3) {
                    Noll = "";
                } 

                id.setText("KP" + Noll + ANN);
            } else {
                id.setText("KP001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }}

