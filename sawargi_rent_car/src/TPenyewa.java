    DefaultTableModel dtm;
    public void showData(){
        String[] kolom={"NO_ID","NIK","NAMA","TELP","ALAMAT"};
 
        dtm = new DefaultTableModel(null,kolom);
        try{
            Statement stmt = koneksi.createStatement();
            String query = "SELECT * FROM tb_penyewa";
            ResultSet rs = stmt.executeQuery(query);
            while(rs.next()){
                String no_id = rs.getString("No_ID");
                String nama = rs.getString("Nik");
                String nik = rs.getString("Nama");
                String telp = rs.getString("Telp");
                String alamat = rs.getString("Alamat");
                
                dtm.addRow(new String[]{no_id,nama,nik,telp,alamat});
            }
        }
        catch (SQLException ex) {
            ex.printStackTrace();
        }
        tb_penyewa.setModel(dtm);
    }
    
