    private void kode() {
        try {
            Statement s = koneksi.createStatement();

            String sql = "SELECT * FROM `tb_penyewa` ORDER by `No_ID` desc";
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                String no = r.getString("No_ID").substring(2);
                String AN = "" + (Integer.parseInt(no) + 1);
                String Nol = "";

                if (AN.length() == 1) {
                    Nol = "00";
                } else if (AN.length() == 2) {
                    Nol = "0";
                } else if (AN.length() == 3) {
                    Nol = "";
                } 

                no_id.setText("KT" + Nol + AN);
            } else {
                no_id.setText("KT001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }}
