    private void NIK_TFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NIK_TFKeyReleased
        try {
            Statement stmt = koneksi.createStatement();
            String sql = "SELECT * FROM tb_penyewa WHERE Nik='"+NIK_TF.getText()+"'";
            ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {
                Nama_TF.setText(rs.getString("Nama"));
                Telp_TF.setText(rs.getString("Telp"));
                Alamat_TA.setText(rs.getString("Alamat"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Member Tidak Ditemukan" + e.getMessage(), "Pesan", JOptionPane.ERROR_MESSAGE);
        }    // TODO add your handling code here:
    }//GEN-LAST:event_NIK_TFKeyReleased

    private void Next_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Next_BtnActionPerformed
        if (Nama_TF.getText().isEmpty() || Telp_TF.getText().isEmpty() || NIK_TF.getText().isEmpty ()|| Alamat_TA.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan data!");
        }else{
            String nama = Nama_TF.getText(); // TODO add your handling code here:
            String telp = Telp_TF.getText();
            String nik = NIK_TF.getText();
            String alamat = Alamat_TA.getText();
            Peminjaman pinjam = new Peminjaman(nama, telp, alamat, nik);           
            menu_utama.DP.removeAll();
            pinjam.setVisible(true);
            menu_utama.DP.add(pinjam).setVisible(true);
            this.dispose();
        }try {
            String sql = "insert into tb_penyewa (No_ID,Nama,Nik,Telp,Alamat) value (?,?,?,?,?)";
            pst = koneksi.prepareStatement(sql);
            pst.setString(1, no_id.getText());
            pst.setString(2, Nama_TF.getText());
            pst.setString(3, NIK_TF.getText());
            pst.setString(4, Telp_TF.getText());
            pst.setString(5, Alamat_TA.getText());
            pst.executeUpdate();
        }catch (SQLException e){
        }
    }//GEN-LAST:event_Next_BtnActionPerformed

